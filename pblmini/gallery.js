function changeImage(direction) {

    currentIndex += direction;

    if (currentIndex < 0) {

        currentIndex = images.length - 1; // Loop back to the last image

    } else if (currentIndex >= images.length) {

        currentIndex = 0; // Loop back to the first image

    }

    updatePanel();

}