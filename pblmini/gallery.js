// Load images into the gallery

function loadImages() {

    const gallery = document.getElementById('photo-gallery');


    images.forEach((image, index) => {

        const photoDiv = document.createElement('div');

        photoDiv.classList.add('photo');


        const imgTag = document.createElement('img');

        imgTag.src = image.src; // Artwork image

        imgTag.alt = image.desc;

        imgTag.onclick = () => openPanel(index); // Pass the index to open the panel with this image


        const descDiv = document.createElement('div');

        descDiv.classList.add('desc');

        descDiv.textContent = image.desc;


        photoDiv.appendChild(imgTag);

        photoDiv.appendChild(descDiv);

        gallery.appendChild(photoDiv);

    });

}


function loadCreators() {

    const container = document.getElementById('creators-container');


    creators.forEach((creator, index) => {

        const creatorDiv = document.createElement('div');

        creatorDiv.classList.add('creator');


        // Creator's photo

        const artistImg = document.createElement('img');

        artistImg.src = creator.src ? creator.src : 'path/to/default-image.jpg';  // Fallback if no artist photo

        artistImg.alt = creator.name;

        artistImg.classList.add('artist');  // Add class 'artist' for styling


        // Creator's name

        const nameTag = document.createElement('h5');

        nameTag.textContent = creator.name;


        // Append artist's image and name to the creator div

        creatorDiv.appendChild(artistImg);

        creatorDiv.appendChild(nameTag);


        // Insert the creator details under the corresponding image

        const photoDiv = document.querySelectorAll('.photo')[index];

        photoDiv.appendChild(creatorDiv);


        // Also insert the creator details inside the panel

        const panelCreatorDiv = creatorDiv.cloneNode(true);  // Clone the div for use in the panel

        const panel = document.getElementById('panel');

        panel.appendChild(panelCreatorDiv);

    });

}


// Open the side panel with the image and artist details

function openPanel(index) {

    const overlay = document.getElementById('overlay');

    const displayedImage = document.getElementById('displayedImage');

    const artistPhoto = document.getElementById('artistPhoto');

    const description = document.getElementById('description');


    // Set the displayed image and description

    displayedImage.src = images[index].src;

    description.textContent = images[index].desc;


    // Find the matching artist for the artwork

    const matchingCreator = creators.find(creator => creator.artworkSrc === images[index].src);


    // If a matching creator is found, display their artist photo

    if (matchingCreator) {

        artistPhoto.src = matchingCreator.src;

        artistPhoto.alt = matchingCreator.name;

    } else {

        artistPhoto.src = ''; // Clear if no artist is found

        artistPhoto.alt = 'Artist photo not available';

    }


    overlay.style.display = 'flex'; // Show the overlay

}


// Close the side panel

function closePanel() {

    const overlay = document.getElementById('overlay');

    overlay.style.display = 'none';

}


